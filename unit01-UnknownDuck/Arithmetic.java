public class Arithmetic {
    public static void main(String[] args) {
        int a = 5;
        int b = 3;

        System.out.println(5 * 5);
        System.out.println(5 * 5.0);
        System.out.println(10 / 3);
        System.out.println(10.0 / 3);
        System.out.println("5 + 3 = " + a + b);
    }
}
