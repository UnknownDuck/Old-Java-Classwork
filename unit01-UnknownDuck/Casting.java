public class Casting {
    public static void main(String[] args){
        char c = 'a';
        int x = c;
        System.out.println(x);
        
        int y =115;
        char d = (char)y;
        System.out.println(d);

        long l = 300000021;
        int z = (int)l;
        System.out.println(z);
        

    }
}
