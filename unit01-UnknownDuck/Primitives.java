public class Primitives {
    public static void main(String[] args) {
        int x = 123;
        long y = 4000000000l;

        float f = 1.23f;
        boolean b = true;
        
        System.out.println("x = " + x);
        System.out.println("y = " + y);
        System.out.println("f = " + f);
        System.out.println("b = " + b);
    }
    
}
